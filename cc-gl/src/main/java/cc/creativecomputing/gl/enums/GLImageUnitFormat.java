package cc.creativecomputing.gl.enums;

import com.jogamp.opengl.GL4;

public enum GLImageUnitFormat {
	RGBA32F(GL4.GL_RGBA32F),
	RGBA16F(GL4.GL_RGBA16F),
	RG32F(GL4.GL_RG32F),
	RG16F(GL4.GL_RG16F),
	R11F_G11F_B10F(GL4.GL_R11F_G11F_B10F),
	R32F(GL4.GL_R32F),
	R16F(GL4.GL_R16F),
	RGBA32UI(GL4.GL_RGBA32UI),
	RGBA16UI(GL4.GL_RGBA16UI),
	RGB10_A2UI(GL4.GL_RGB10_A2UI),
	RGBA8UI(GL4.GL_RGBA8UI),
	RG32UI(GL4.GL_RG32UI),
	RG16UI(GL4.GL_RG16UI),
	RG8UI(GL4.GL_RG8UI),
	R32UI(GL4.GL_R32UI),
	R16UI(GL4.GL_R16UI),
	R8UI(GL4.GL_R8UI),
	RGBA32I(GL4.GL_RGBA32I),
	RGBA16I(GL4.GL_RGBA16I),
	RGBA8I(GL4.GL_RGBA8I),
	RG32I(GL4.GL_RG32I),
	RG16I(GL4.GL_RG16I),
	RG8I(GL4.GL_RG8I),
	R32I(GL4.GL_R32I),
	R16I(GL4.GL_R16I),
	R8I(GL4.GL_R8I),
	RGBA16(GL4.GL_RGBA16),
	RGB10_A2(GL4.GL_RGB10_A2),
	RGBA8(GL4.GL_RGBA8),
	RG16(GL4.GL_RG16),
	RG8(GL4.GL_RG8),
	R16(GL4.GL_R16),
	R8(GL4.GL_R8),
	RGBA16_SNORM(GL4.GL_RGBA16_SNORM),
	RGBA8_SNORM(GL4.GL_RGBA8_SNORM),
	RG16_SNORM(GL4.GL_RG16_SNORM),
	RG8_SNORM(GL4.GL_RG8_SNORM),
	R16_SNORM(GL4.GL_R16_SNORM),
	R8_SNORM(GL4.GL_R8_SNORM);
	
	private int _myGLID;
	
	GLImageUnitFormat(int theGLID){
		_myGLID = theGLID;
	}
	
	public int glID(){
		return _myGLID;
	}
	
	public static GLImageUnitFormat fromGLID(int theGLID){
		switch(theGLID){
		case GL4.GL_RGBA32F:return RGBA32F;
		case GL4.GL_RGBA16F:return RGBA16F;
		case GL4.GL_RG32F:return RG32F;
		case GL4.GL_RG16F:return RG16F;
		case GL4.GL_R11F_G11F_B10F:return R11F_G11F_B10F;
		case GL4.GL_R32F:return R32F;
		case GL4.GL_R16F:return R16F;
		case GL4.GL_RGBA32UI:return RGBA32UI;
		case GL4.GL_RGBA16UI:return RGBA16UI;
		case GL4.GL_RGB10_A2UI:return RGB10_A2UI;
		case GL4.GL_RGBA8UI:return RGBA8UI;
		case GL4.GL_RG32UI:return RG32UI;
		case GL4.GL_RG16UI:return RG16UI;
		case GL4.GL_RG8UI:return RG8UI;
		case GL4.GL_R32UI:return R32UI;
		case GL4.GL_R16UI:return R16UI;
		case GL4.GL_R8UI:return R8UI;
		case GL4.GL_RGBA32I:return RGBA32I;
		case GL4.GL_RGBA16I:return RGBA16I;
		case GL4.GL_RGBA8I:return RGBA8I;
		case GL4.GL_RG32I:return RG32I;
		case GL4.GL_RG16I:return RG16I;
		case GL4.GL_RG8I:return RG8I;
		case GL4.GL_R32I:return R32I;
		case GL4.GL_R16I:return R16I;
		case GL4.GL_R8I:return R8I;
		case GL4.GL_RGBA16:return RGBA16;
		case GL4.GL_RGB10_A2:return RGB10_A2;
		case GL4.GL_RGBA8:return RGBA8;
		case GL4.GL_RG16:return RG16;
		case GL4.GL_RG8:return RG8;
		case GL4.GL_R16:return R16;
		case GL4.GL_R8:return R8;
		case GL4.GL_RGBA16_SNORM:return RGBA16_SNORM;
		case GL4.GL_RGBA8_SNORM:return RGBA8_SNORM;
		case GL4.GL_RG16_SNORM:return RG16_SNORM;
		case GL4.GL_RG8_SNORM:return RG8_SNORM;
		case GL4.GL_R16_SNORM:return R16_SNORM;
		case GL4.GL_R8_SNORM:return R8_SNORM;
		}
		return null;
	}
}

